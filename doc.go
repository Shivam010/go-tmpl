// file, a license and a readme. The Package is initialised with go modules,
// and a doc file.
//
// The Project structure:
// ```
// 	.
// 	├── .github
// 	│   ├── workflows
// 	│   │   └── build.yml
// 	│   └── FUNDING.yml
// 	├── .gitignore
// 	├── CODE_OF_CONDUCT.md
// 	├── CONTRIBUTING.md
// 	├── LICENSE
// 	├── Makefile
// 	├── README.md
// 	├── doc.go
// 	├── go.mod
// 	├── main.go
// 	└── main_test.go
// ```
